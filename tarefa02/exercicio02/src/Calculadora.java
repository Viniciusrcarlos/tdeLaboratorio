public class Calculadora {
    public float primeiroNumero;
    public float segundoNumero;

    float somar(){
        return (primeiroNumero + segundoNumero);
    }float subtrair(){
        return (primeiroNumero - segundoNumero);
    }float multiplicar(){
        return (primeiroNumero * segundoNumero);
    }float dividir(){
        return (primeiroNumero / segundoNumero);
    }
}
